package problem8_ShapesVolume;

public class Cylinder {
    private double radius;
    private double heigth;

    public Cylinder(double radius, double heigth) {
        this.radius = radius;
        this.heigth = heigth;
    }

    public double getRadius() {
        return this.radius;
    }

    public double getHeigth() {
        return this.heigth;
    }
}